# Python program showing how to use string modulo operator(%)
print("Two digit value : %6d, Float value : %5.7f" % (1, 354.1732)) #the number written indicates the gap(empty space)
print("Total students : %3d, Boys : %2d" % (240, 120)) # print
# integer value
print("Octal of %2d is %7.3o" % (25, 25)) # print octal value
print("Gravitational constant, G = %10.3E" % (6.6743e-11)) # print E for exponential
# exponential value